#!/usr/bin/env python3
# -*- coding: utf-8 -*- 

if __name__ == '__main__':
    a = 10
    D = 9 + 4*a
    if D > 0:
        x1 = (5 + D**0.5) / 2
        x2 = (5 - D**0.5) / 2
        print(f"Для a = {a} неравенство имеет два корня: x1 = {x1} и x2 = {x2}")
    elif D == 0:
        x = 5 / 2
        print(f"Для a = {a} неравенство имеет один корень: x = {x}")
    else:
        print(f"Для a = {a} неравенство не имеет корней")
